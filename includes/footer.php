<script>
    document.addEventListener('DOMContentLoaded', function() {
        const dropdown = document.querySelector('.megamenu');
        const toggle = dropdown.querySelector('[data-bs-toggle="dropdown"]');

        toggle.addEventListener('click', function(e) {
            if (window.innerWidth < 992) { // only for mobile/tablet
                e.preventDefault();
                dropdown.classList.toggle('show');
            }
        });

        document.addEventListener('click', function(e) {
            if (!dropdown.contains(e.target)) {
                dropdown.classList.remove('show');
            }
        });
    });
</script>

<script>
    document.addEventListener("DOMContentLoaded", function() {
        const nav = document.querySelector(".mobile-nav");

        window.addEventListener("scroll", function() {
            if (window.scrollY > 66) {
                nav.classList.add("scrolled");
            } else {
                nav.classList.remove("scrolled");
            }
        });
    });
</script>

<script>
    document.addEventListener('DOMContentLoaded', () => {
        const navLinks = document.querySelector('.navbar .nav-links');
        const callBtn = document.getElementById('callBtn');

        if (!navLinks || !callBtn) return;

        function updateCallBtnZIndex() {
            const left = window.getComputedStyle(navLinks).getPropertyValue('left');
            // console.log('nav-links left:', left); // uncomment for debugging

            if (left === '0px') {
                callBtn.style.zIndex = '1'; // put behind the menu
            } else {
                callBtn.style.zIndex = '111'; // keep it on top when menu is closed
            }
        }

        // Check every 200ms to detect CSS-only changes
        setInterval(updateCallBtnZIndex, 100);
    });
</script>